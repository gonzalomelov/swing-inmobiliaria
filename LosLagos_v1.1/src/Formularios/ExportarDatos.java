    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (!(c.isSelected() | p.isSelected() | s.isSelected())){
            JOptionPane.showMessageDialog(this, "Debe seleccionar por lo menos un campo a exportar","Error",JOptionPane.INFORMATION_MESSAGE);
            return; 
        }
        
        boolean bc = c.isSelected();
        boolean bp = p.isSelected();
        boolean bs = s.isSelected();
        
        String path = abrirImagen();
        
        if (path.equals("")){
            JOptionPane.showMessageDialog(null,"Advertencia", "Debe ingresar un nombre al archivo para crearlo", JOptionPane.WARNING_MESSAGE);
            return;
            
        }
        
        Fabrica f = new Fabrica();
        IControladorDatos iCD = f.getIControladorDatos();
        try {
            iCD.exportarDatos(path, bc, bp, bs);
        } catch (Exception ex) {
            Logger.getLogger(ExportarDatos.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }//GEN-LAST:event_jButton2ActionPerformed

